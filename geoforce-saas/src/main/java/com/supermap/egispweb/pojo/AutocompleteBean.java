package com.supermap.egispweb.pojo;
/**
 * jquery autoComplete自动提醒 bean
* @ClassName: AutocompleteBean
* @author WangShuang
* @date 2012-12-12 上午11:11:41
 */
public class AutocompleteBean {

	private String id;
	private String code;
	private String name;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
